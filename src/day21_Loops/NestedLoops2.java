package day21_Loops;

public class NestedLoops2 {
    public static void main(String[] args) {

        for (int j = 0; j < 4; j++) {

            for (int i = 0; i < 3; i++) {
                System.out.println("Hello World");
            }

            System.out.println("Hello Universe");

        }










    }
}
