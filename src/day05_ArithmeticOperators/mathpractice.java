package day05_ArithmeticOperators;

public class mathpractice {

    public static void main(String[] args) {

        System.out.println(4 + 5);
        System.out.println(4 * 5);
        System.out.println( 2 *5 ); // int * int
        System.out.println(2 * 5.5);// int * double --> double 11.0

        System.out.println(10/3); // int /int whole number
        System.out.println(10/3.0);// int / double--> double result

        System.out.println(5 % 2); // 2 + 2 -> 2 went into 5 twice ,the is one(1) left over"Modulus"
        System.out.println( 4 % 2); // no left over because 2 divides into 4 evenly left with 0
        System.out.println(6 % 2);// still even no left over 6/2 = 3 even
        System.out.println(7 % 2);
        System.out.println(4 % 10);//4 is left over because you can't smaller number in bigger one.










    }
}
