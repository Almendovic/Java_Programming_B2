package day06_Operators;

public class UpdatingVariable {
    public static void main(String[] args) {


        int n = 10;
        n = 20; //reassign


        int a = 3;// a+4 -> 3+4= 7
        a= a +4 ;


        int b=15;// b = b+9
        b +=9;
        b +=1;
        System.out.println(b);

        int z = 10;
        z++;
        System.out.println(z);




    }

}
