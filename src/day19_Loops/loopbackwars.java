package day19_Loops;

public class loopbackwars {

    public static void main(String[] args) {


        for (int i = 50; i >= 0; i--) {
            System.out.print(i + " ");
        }
        System.out.println();

        for (char letter = 'Z'; letter >= 'A'; letter--) {
            System.out.print(letter + " ");
        }


    }
}
